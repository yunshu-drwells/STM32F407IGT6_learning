void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)  //按键中断处理函数
{
    if(GPIO_Pin == KEY0_Pin)
    {
      //KEY0是否按下
			if(GPIO_PIN_RESET == HAL_GPIO_ReadPin(KEY0_GPIO_Port, KEY0_Pin)){
				HAL_Delay(20);  //去抖动
				if(GPIO_PIN_RESET == HAL_GPIO_ReadPin(KEY0_GPIO_Port, KEY0_Pin)){
					printf("KEY0: Send data! \n");
					res = can_send_msg(0x12, canbuf, 8); /* ID = 0x12, 发送8个字节 */
					if(res){
						printf("send failed!\n");
					}else{
						printf("send successed!\n");
					}
				}
			}
    }
		if(GPIO_Pin == KEY1_Pin)
    {
      //KEY1是否按下
			if(GPIO_PIN_RESET == HAL_GPIO_ReadPin(KEY1_GPIO_Port, KEY1_Pin)){
				HAL_Delay(20);  //去抖动
				if(GPIO_PIN_RESET == HAL_GPIO_ReadPin(KEY1_GPIO_Port, KEY1_Pin)){
					printf("KEY1: Turn can1 to Loopback mode!\n");
					mode = 1;
					/* CAN初始化, 普通(0)/回环(1)模式, 波特率500Kbps */ 
					can_init(CAN_SJW_1TQ, CAN_BS2_6TQ, CAN_BS1_7TQ, 6, mode ? CAN_MODE_LOOPBACK : CAN_MODE_NORMAL);
				}
			}
    }
		if(GPIO_Pin == KEY2_Pin)
    {
      //KEY2是否按下
			if(GPIO_PIN_RESET == HAL_GPIO_ReadPin(KEY2_GPIO_Port, KEY2_Pin)){
				HAL_Delay(20);  //去抖动
				if(GPIO_PIN_RESET == HAL_GPIO_ReadPin(KEY2_GPIO_Port, KEY2_Pin)){
					printf("KEY1: Turn can1 to Normal mode!\n");
					mode = 0;
					/* CAN初始化, 普通(0)/回环(1)模式, 波特率500Kbps */ 
					can_init(CAN_SJW_1TQ, CAN_BS2_6TQ, CAN_BS1_7TQ, 6, mode ? CAN_MODE_LOOPBACK : CAN_MODE_NORMAL);
				}
			}
    }
}
